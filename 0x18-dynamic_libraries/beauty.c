#include <stdio.h>

/* add - add two integers
 * @a: input integer
 * @b: input integer
 * Return: the sum
 */
int add(int a, int b)
{
	return (a + b);
}

/* sub - sub two integers
 * @a: input integer
 * @b: input integer
 * Return: the sub
 */
int sub(int a, int b)
{
	return (a - b);
}

/* mul - mul two integers
 * @a: input integer
 * @b: input integer
 * Return: the mul
*/
int mul(int a, int b)
{
        return (a * b);
}

/* div - div two integers
 * @a: input integer
 * @b: input integer
 * Return: the div
*/
int div(int a, int b)
{
        return (a / b);
}

/* mod - mod two integers
 * @a: input integer
 * @b: input integer
 * Return: the mod
*/
int mod(int a, int b)
{
        return (a % b);
}
